/* GET CHARACTER DATA */
static const char * const *get_char(char c) {
    int idx;
    c = (char)toupper((unsigned char)c);
    idx = (int)(unsigned char)c - 32;
    if (idx < 0 || idx > 58) idx = 0;
    return &font_data[idx * FONT_HEIGHT];
}

/* RENDER TEXT AS ASCII ART */
static void render(const char *text) {
    int row;
    const char *p;
    for (row = 0; row < FONT_HEIGHT; row++) {
        for (p = text; *p; p++) {
            printf("%s ", get_char(*p)[row]);
        }
        printf("\n");
    }
}

/* GET USER INPUT */
static void get_input(char *buf, int size) {
    if (fgets(buf, size, stdin)) {
        int len = (int)strlen(buf);
        if (len > 0 && buf[len-1] == '\n') buf[len-1] = '\0';
    }
}

int main(void) {
    char text[MAX_TEXT];

    printf("FIGLET - ASCII ART TEXT\n");
    printf("=======================\n\n");

    while (1) {
        printf("ENTER TEXT (OR Q TO QUIT): ");
        get_input(text, sizeof(text));

        if ((text[0] == 'Q' || text[0] == 'q') && text[1] == '\0') {
            printf("GOODBYE!\n");
            break;
        }

        if (text[0]) {
            printf("\n");
            render(text);
            printf("\n");
        }
    }

    return 0;
}
